export const getPermitsByStatus = (status: PermitStatus) => 
  mockPermits.filter(p => p.status === status);

export const getPermitsByRole = (role: string) => {
  switch (role) {
    case 'contractor':
      return mockPermits;
    case 'helpdesk':
      return mockPermits.filter(p => ['submitted', 'approved', 'closed'].includes(p.status) || p.helpdeskApproval.status === 'pending');
    case 'pm':
      return mockPermits.filter(p => p.status === 'pending_pm' || p.pmApproval.status !== null);
    case 'pd':
      return mockPermits.filter(p => p.status === 'pending_pd' || p.pdApproval.status !== null);
    case 'it':
      return mockPermits.filter(p => p.status === 'pending_it' || p.itApproval.status !== null);
    case 'pm_service':
      return mockPermits.filter(p => p.status === 'pending_pm_service' || p.pmServiceApproval.status !== null);
    default:
      return mockPermits;
  }
};

export const getStatsForRole = (role: string) => {
  const permits = getPermitsByRole(role);
  return {
    total: permits.length,
    pending: permits.filter(p => p.status.startsWith('pending') || p.status === 'submitted' || p.status === 'under_review').length,
    approved: permits.filter(p => p.status === 'approved').length,
    rejected: permits.filter(p => p.status === 'rejected').length,
    closed: permits.filter(p => p.status === 'closed').length,
  };
};
